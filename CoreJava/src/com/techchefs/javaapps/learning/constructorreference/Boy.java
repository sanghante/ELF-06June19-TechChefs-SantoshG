package com.techchefs.javaapps.learning.constructorreference;

public class Boy {
	
	int age;
	String name;
	double height;
	
	
	/**
	 * @param age
	 * @param name
	 * @param height
	 */
	public Boy(int age, String name, double height) {
		super();
		this.age = age;
		this.name = name;
		this.height = height;
	}
	
}
